    private void btnAñadit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadit1ActionPerformed
        // TODO add your handling code here:
        lblMensaje.setText("");
        empleado aux[] = new empleado[registro[0].length*registro[1].length];
	int k=0;
                
	for(int l=0; l<registro[0].length; l++){
		for(int m=0; m<registro[1].length; m++){
                    aux[k]= registro[l][m];
                    k++;
		}			
	}
         for(int i = 0; i < aux.length; i++){
                for(int j = 0; j < aux.length - 1; j++){
                    
                    if(aux[i].obtenerCodigo().compareTo(aux[j].obtenerCodigo()) < 0){
                       empleado temp = aux[i];
                       aux[i] = aux[j];
                       aux[j] = temp;
                    }
                    
                }
         }
      
        k=0;
	for(int l=0; l<registro[0].length; l++){
			for(int m=0; m<registro[1].length; m++){			
				registro[m][l] = aux[k];
				k++;
			}			
		}
        
    }//GEN-LAST:event_btnAñadit1ActionPerformed

    private void btnAñadit2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadit2ActionPerformed
        // TODO add your handling code here:
        lblMensaje.setText("");
        String aux= "";
        for (int k = 0; k < registro.length; k++) {
            for (int j = 0; j < registro.length; j++) {
                aux += "Nombre: "+registro[j][k].obtenerNombre()+"\nSaldo: "+registro[j][k].obtenerSueldo()+"\nNúmero de cuenta: "+registro[j][k].obtenerCodigo()+"\n\n";
            }
        }       
        jTextArea1.setText(aux);
        
    }//GEN-LAST:event_btnAñadit2ActionPerformed

