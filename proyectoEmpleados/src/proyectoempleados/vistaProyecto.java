    private void btnAñaditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñaditActionPerformed
        // TODO add your handling code here:
        try{
            registro[i][j] = new empleado(txtNombre.getText(),Integer.parseInt(txtSueldo.getText()));
            registro[i][j].setearCodigo(((char) (Math.random()*25 + 65))+""+i);
            lblMensaje.setText("Ingreso correcto. Su código asignado es "+registro[i][j].obtenerCodigo());
            
            if(i==1){
                j++;
                i=0;           
            }
            else
                i++;
                  
        }catch(Exception e){
            lblMensaje.setText("Ha ocurrido un error en el ingreso de datos.");
        }
    }//GEN-LAST:event_btnAñaditActionPerformed

